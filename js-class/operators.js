/* Arithmetic operators 
    + Addition
    - Subtraction
    * Multiplication
    / Division
    % Modulus 
    ++ Before: Increment and then return value | After: Return value and then increment
    -- Before: Decrement and then return value | After: Return value and then drecrement 
*/

console.log(2 * 2) // 4
console.log(2 / 2) // 1
console.log(2 % 1.5) // 0.5
console.log(2 + 2) // 4
console.log(2 - 2) // 0
